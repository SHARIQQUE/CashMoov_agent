package com.agent.cashmoovui.model;

public class OverdraftViewconfigModel {

    public String channelTypeName;
    public String ewalletServicesName;
    public String servicesCategoryName;
    public String servicesProviderName;
    public String status;





    public String getChannelTypeName() {
        return channelTypeName;
    }

    public void setChannelTypeName(String channelTypeName) {
        this.channelTypeName = channelTypeName;
    }

    public String getEwalletServicesName() {
        return ewalletServicesName;
    }

    public void setEwalletServicesName(String ewalletServicesName) {
        this.ewalletServicesName = ewalletServicesName;
    }

    public String getServicesCategoryName() {
        return servicesCategoryName;
    }

    public void setServicesCategoryName(String servicesCategoryName) {
        this.servicesCategoryName = servicesCategoryName;
    }

    public String getServicesProviderName() {
        return servicesProviderName;
    }

    public void setServicesProviderName(String servicesProviderName) {
        this.servicesProviderName = servicesProviderName;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
